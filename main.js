function createGame(player1, hour, player2, group) {
    return `
    <li>
        <img src="./assets/icon-${player1}.svg" alt="Bandeira do ${player1}" title="${player1}">
        <strong><h6>Grupo ${group}</h6>${hour}</strong>
        <img src="./assets/icon-${player2}.svg" alt="Bandeira da ${player2}" title="${player2}">
    </li> 
    `
}

let delay = -0.4;

function createCard(date, day, games) {
    delay += 0.4;
    return `
    <div class="card" style="animation-delay ${delay}s">
        <h2>${date}<span>${day}</span></h2>
        <ul>
            ${games}
        </ul> 
    </div>
    `
}

var retCards = '';
var retGame = '';
var dataControll = '';


for (let index = 0; index < gameAgend.length; index++) {

    retGame = '';

    let _retCards = gameAgend[index].date;

    if (dataControll != _retCards) {

        let _retGame = gameAgend.filter(_date => _date.date === _retCards);

        _retGame.forEach(el => {
            retGame += createGame(el.player1, el.hour, el.player2, el.group);
        });

        retCards +=
            createCard(gameAgend[index].date, gameAgend[index].day, retGame);

        dataControll = _retCards;
    }

}
document.querySelector("#cards").innerHTML = retCards;





